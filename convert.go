var std = newDict()

type dict struct {
	s2t map[rune]rune
	t2s map[rune]rune
}

func (d *dict) s2tRune(char rune) rune {
	if s, ok := d.s2t[char]; ok {
		return s
	}
	return char
}

func (d *dict) t2sRune(char rune) rune {
	if s, ok := d.t2s[char]; ok {
		return s
	}
	return char
}

func newDict() *dict {
	d := &dict{
		s2t: make(map[rune]rune),
		t2s: make(map[rune]rune),
	}
	for i := 0; i < len(simplifiedDict) && i < len(traditionalDict); i++ {
		s, t := simplifiedDict[i], traditionalDict[i]
		d.s2t[s] = t
		d.t2s[t] = s
	}
	return d
}

func read(s string, t func(char rune) rune) string {
	r := []rune(s)
	for i := 0; i < len(r); i++ {
		r[i] = t(r[i])
	}
	return string(r)
}

// S2T simplified to traditional
func S2T(s string) string {
	return read(s, std.s2tRune)
}

// T2S traditional to simplified
func T2S(s string) string {
	return read(s, std.t2sRune)
}
